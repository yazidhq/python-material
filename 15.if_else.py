# if dan else statement

#  1. ifnya
#  2. kondisinya
#  3. aksinya

nama = input("Siapa nama anda?")

# if statement
if nama == "yazid":
    print(f"Nama anda {nama}")

print(f"Terimakasih {nama}")

# if else statement
if nama == "yazid":
    print(f"Nama anda {nama}")
else:
    print(f"Kamu bukan yazid")
print("Akhir Program")

# if else if (elif) statement
if nama == "yazid":
    print(f"Nama anda {nama}")
elif nama == "otong":
    print(f"Nama anda {nama}")
else:
    print(f"Kamu bukan yazid/ otong")
print("Akhir Program")
