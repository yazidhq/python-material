# Perulangan

# FOR
# for dengan list
angka1 = [0, 1, 2, 3, 4]  # list
for i in angka1:
    print(f"Angka: {i}")
print("Akhir program")

# for dengan range
angka2 = range(5)
for i in angka2:
    print(f"Angka: {i}")
print("Akhir program")

angka3 = range(1, 10)
for i in angka3:
    print(f"Angka: {i}")
print("Akhir program")

# for dengan string
data_str = "Saya Yazid"
for huruf in data_str:
    print(huruf)

# WHILE
# while kondisi:
    # aksi 1
    # aksi 2
# akhir program
angka = 0
while angka < 5:
    angka += 1
    print(f"Ini angka {angka}")
print("Akhir program")
